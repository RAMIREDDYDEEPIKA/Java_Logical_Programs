package com.logical_and_operators;

public class BitwiseAndOrXor {

	public static void main(String[] args) {
		 int n1=4;
		 int n2=2;
		 int and=n1 & n2;
		 int or=n1 | n2;
		 int xor=n1 ^ n2;
		 System.out.println("Result of Bitwise and operator: "+and);
		 System.out.println("Result of Bitwise or operator: "+or);
		 System.out.println("Result of Bitwise xor operator: "+xor);
		 

	}

}
